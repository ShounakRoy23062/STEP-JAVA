public class CalProandLoss{
	public static void main (String[] args){
		int sp = 191;
		int cp = 129;
		
		int profit = sp - cp;
		double profit_percent = ((double)profit/cp) * 100;
		
		System.out.println("The Cost Price is INR " +cp+ " and Selling Price is INR " +sp+ ". The Profit is INR " +profit+ " and the Profit Percentage is " +profit_percent+ "%.");
    }
}	
		
		
